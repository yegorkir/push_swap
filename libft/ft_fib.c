int		ft_fib(int n)
{
	int fib[n + 1];
	int i;

	if (n < 0)
		return (0);
	i = 1;
	fib[0] = 0;
	fib[1] = 1;
	while (++i <= n)
		fib[i] = fib[i - 2] + fib[i - 1];
	return (fib[n]);
}
