char	*ft_strcat(char *dest, const char *src)
{
	int i;

	i = 0;
	while (*dest)
	{
		++dest;
		++i;
	}
	while (*src)
	{
		*dest = *src;
		++src;
		++dest;
		++i;
	}
	*dest = '\0';
	return (dest - i);
}
